# 나의 풀이
def xmastree(n):
    return ['_' * (n-(i+1)) + '#'*(2*i+1) + '_' * (n-(i+1)) for i in range(n)] + ['_'*(n-1) + '#' + '_'*(n-1) for i in range(2)]

# 다른 사람의 풀이
def xmastree1(n):
    return ['_' * (n-(i+1)) + '#'*(2*i+1) + '_' * (n-(i+1)) for i in range(n)] + ['_'*(n-1) + '#' + '_'*(n-1) for i in range(2)]

def xMasTree2(n):
    w = list(range(1, n * 2, 2)) + [1, 1]
    return [f"{'#' * i}".center(n * 2 - 1, "_") for i in w]

