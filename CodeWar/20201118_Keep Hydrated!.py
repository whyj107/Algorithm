def litres(time):
    return int(time * 0.5)

print(litres(2), 1)
print(litres(1.4), 0)
print(litres(12.3), 6)
print(litres(0.82), 0)
print(litres(11.8), 5)
print(litres(1787), 893)
print(litres(0), 0)